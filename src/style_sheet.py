"""Generate per-character stylesheet images (turnaround + expressions).

Stylesheets lock visual identity for downstream panel generation. They are
generated by feeding selected.png back to Gemini as a reference image and
asking for a 4-pose turnaround sheet and a 6-cell expression grid.

Outputs (per character):
  - characters/<id>/selected.png            (copied from candidates/candidate_N.png)
  - characters/<id>/stylesheet_turnaround.png
  - characters/<id>/stylesheet_expressions.png
"""
from __future__ import annotations

import shutil
import sys
import time
from pathlib import Path

from .image_generator import generate_image
from .project import Character, ProjectContext, load


DEFAULT_POSES = [
    "Front view, arms relaxed at sides",
    "Three-quarter view (facing camera left at about 45 degrees)",
    "Pure side profile (facing camera left)",
    "Back view from behind",
]
DEFAULT_EXPRESSIONS = [
    "neutral blank",
    "performative smile",
    "genuine smile reaching the eyes",
    "hollow thousand-yard stare",
    "frozen frightened apprehension",
    "quiet resignation",
]


TURNAROUND_TEMPLATE = """Character design turnaround sheet on a single image: four full-body views of the SAME character lined up horizontally left to right, identical proportions, identical clothing, identical hair, identical face.

Character (MUST match the reference image provided): {description}. The character wears {outfit}.

Poses, left to right:
{poses_block}

Expression: neutral and consistent across all four poses.
Era: {era}.
Style: {style}, identical drawing style for all four.
Background: plain near-white background with a common faint ground shadow line beneath all figures; no scenery, no props.
Lighting: soft even studio light.
Aspect ratio: 16:9.
Do not include: text, labels, captions, watermarks, extra characters, differing outfits, distorted anatomy, extra fingers."""


EXPRESSION_TEMPLATE = """Character expression sheet on a single image: a 2-row by 3-column grid of six head-and-shoulders portraits of the SAME character. Identical drawing style and character, only expressions differ.

Character (MUST match the reference image provided): {description}.

Six expressions (row-major order):
{expressions_block}

Era: {era}.
Style: {style}. Each cell shares the same lighting and framing.
Framing: head-and-shoulders only, face occupies most of each cell.
Background: plain near-white background per cell.
Lighting: soft even front light.
Aspect ratio: 3:2.
Do not include: text, labels, captions, watermarks, extra characters, full body, distorted anatomy, extra fingers."""


def _numbered_block(items: list[str]) -> str:
    return "\n".join(f"{i+1}. {item}." for i, item in enumerate(items))


def turnaround_prompt(project: ProjectContext, character: Character) -> str:
    poses = character.poses or DEFAULT_POSES
    return TURNAROUND_TEMPLATE.format(
        description=character.description_en,
        outfit=character.outfit_en or "era-appropriate clothing",
        era=project.era,
        style=project.style_description,
        poses_block=_numbered_block(poses[:4]),
    )


def expression_prompt(project: ProjectContext, character: Character) -> str:
    exprs = character.expressions or DEFAULT_EXPRESSIONS
    # Group as Top row / Bottom row for the grid layout.
    top = ", ".join(f"({i+1}) {e}" for i, e in enumerate(exprs[:3]))
    bottom = ", ".join(f"({i+4}) {e}" for i, e in enumerate(exprs[3:6]))
    block = f"Top row — {top}.\nBottom row — {bottom}."
    return EXPRESSION_TEMPLATE.format(
        description=character.description_en,
        era=project.era,
        style=project.style_description,
        expressions_block=block,
    )


def ensure_selected(project: ProjectContext, character: Character) -> Path | None:
    """Make sure characters/<id>/selected.png exists.

    - If selected.png already exists, leave it alone (preserves byte equivalence
      after migration even if selected_candidate is a guess).
    - Else if selected_candidate is set, copy candidates/candidate_<N>.png.
    - Else return None — pipeline should pause for human selection.
    """
    selected = character.selected_png(project.project_dir)
    if selected.exists():
        return selected
    n = character.selected_candidate
    if n is None:
        return None
    src = character.candidates_dir(project.project_dir) / f"candidate_{n}.png"
    if not src.exists():
        return None
    selected.parent.mkdir(parents=True, exist_ok=True)
    shutil.copyfile(src, selected)
    return selected


def generate_stylesheet(
    project: ProjectContext,
    character: Character,
    inter_call_delay_s: float = 6.0,
    force: bool = False,
) -> tuple[Path, Path]:
    selected = ensure_selected(project, character)
    if selected is None:
        raise RuntimeError(
            f"{character.id}: no selected.png and selected_candidate not set — "
            "pick a candidate first."
        )

    turnaround = character.stylesheet_turnaround(project.project_dir)
    expressions = character.stylesheet_expression(project.project_dir)

    if not turnaround.exists() or force:
        print(f"[{character.id}] turnaround → {turnaround}")
        generate_image(turnaround_prompt(project, character), turnaround, reference_images=[selected])
        time.sleep(inter_call_delay_s)
    if not expressions.exists() or force:
        print(f"[{character.id}] expressions → {expressions}")
        generate_image(expression_prompt(project, character), expressions, reference_images=[selected])

    return turnaround, expressions


def main() -> None:
    if len(sys.argv) < 2:
        print("Usage: python -m src.style_sheet <project_id> [<character_id>] [--force]", file=sys.stderr)
        sys.exit(1)
    project = load(sys.argv[1])
    force = "--force" in sys.argv
    char_id = next((a for a in sys.argv[2:] if not a.startswith("--")), None)

    targets = [project.character(char_id)] if char_id else project.characters
    targets = [c for c in targets if c is not None]
    for c in targets:
        try:
            generate_stylesheet(project, c, force=force)
        except RuntimeError as e:
            print(f"  ! skip {c.id}: {e}")


if __name__ == "__main__":
    main()
